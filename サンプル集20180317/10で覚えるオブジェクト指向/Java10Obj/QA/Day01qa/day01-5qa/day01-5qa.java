class Account {
  String number;
  int amount;
  String secretNumber;
  
  void setNumber(String number){
    this.number = number;
  }
  
  String getNumber() {
    return this.number;
  }
  
  int getAmount() {
    return this.amount;
  }
  
  void setSecretNumber(String secretNumber) {
    this.secretNumber = secretNumber;
  }
}